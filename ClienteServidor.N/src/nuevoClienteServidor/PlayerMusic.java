    /**
 * Initializes the media player with the specified song file.
 *
 * @param songFilePath The song file path.
 */
    private void initializeMediaPlayer(String songFilePath) {

        
        Platform.runLater(() -> {
            Media media = new Media(new File(songFilePath).toURI().toString());
            mediaPlayer = new MediaPlayer(media);
        });
    }

  /**
 * Play the song currently loaded in the media player.
 */
    private void playSong() {
        if (mediaPlayer != null) {
            Platform.runLater(() -> mediaPlayer.play());
        }
    }

    /**
 * Pause playback of the song currently loaded in the media player.
 */
    private void pauseSong() {
        if (mediaPlayer != null) {
            Platform.runLater(() -> mediaPlayer.pause());
        }
    }
/**
 * Stops playing the song currently loaded in the media player.
 * Additionally, it frees up the resources associated with the player.
 */
    private void stopSong() {
        if (mediaPlayer != null) {
            Platform.runLater(() -> {
                mediaPlayer.stop();
                mediaPlayer.dispose();
            });
        }
    }

    public MediaPlayer getMediaPlayer() {
        return mediaPlayer;
    }

